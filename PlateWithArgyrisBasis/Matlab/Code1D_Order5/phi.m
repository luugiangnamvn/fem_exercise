function [A] = phi(cases, x)
switch cases
    case 1
        A = 1/2-15/16*x+5/8*x^3-3/16*x^5;
    case 2
        A = 1/2+15/16*x-5/8*x^3+3/16*x^5;
    case 3
        A = 5/16-7/16*x-3/8*x^2+5/8*x^3+1/16*x^4-3/16*x^5;
    case 4
        A = -5/16-7/16*x+3/8*x^2+5/8*x^3-1/16*x^4-3/16*x^5;
    case 5
        A = 1/16-1/16*x-1/8*x^2+1/8*x^3+1/16*x^4-1/16*x^5;
    case 6
        A = 1/16+1/16*x-1/8*x^2-1/8*x^3+1/16*x^4+1/16*x^5;
end
